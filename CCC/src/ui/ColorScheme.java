package ui;


import java.awt.Color;


/**
 * 
 * This class provides a central place to get GUI colors
 * 
 * Example usage: new LineBorder(ColorScheme.BLUE)
 * 
 * @author Oussama Zgheb
 * @version 1.0
 * 
 */

public class ColorScheme {
	
	public static final Color FONTWHITE = Color.decode("#FFFFFF");
	public static final Color FONTBLACK = Color.decode("#000000");

	public static final Color GOLD = Color.decode("#E8FD49");
	public static final Color GOLD_DARK = Color.decode("#978D00");
	public static final Color ACTIVE_GREEN = Color.decode("#BEF349");
	public static final Color INACTIVE_ORANGE = Color.decode("#FF8C00");


}
